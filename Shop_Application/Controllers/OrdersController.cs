using Shop_Application.Models;

namespace Shop_Application.Controllers
{
    [Authorize]
    public class OrdersController : Controller
    {
        private readonly ApplicationDbContext _context;

        public OrdersController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Orders
        public async Task<IActionResult> Index()
        {
            var applicationDbContext = _context.Order.Include(o => o.Car);
            return View(await applicationDbContext.ToListAsync());
        }

        // GET: Orders/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var order = await _context.Order
                .Include(o => o.Car)
                .FirstOrDefaultAsync(m => m.Id == id);
            if (order == null)
            {
                return NotFound();
            }

            return View(order);
        }

        // GET: Orders/Create
        public IActionResult Create(string car)
        {
            int.TryParse(car, out int iCarId);
            ViewData["Cars"] = new SelectList(_context.Car, "Id", "CarMark");
            Order order = new Order();
            order.Car = _context.Car.FirstOrDefault(Car => Car.Id == iCarId);
            order.CarId = iCarId;
            order.PriceOfOrder = order.Car.PriceOfCar;
            return View(order);
        }

